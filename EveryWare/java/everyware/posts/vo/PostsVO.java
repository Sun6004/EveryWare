package everyware.posts.vo;

public class PostsVO {

	private int post_id;
	private String board_id;
	private String emp_id;
	private String post_title;
	private String post_content;
	private String post_date;
	private String post_cnt;
	private String post_subtitle;

	public int getPost_id() {
		return post_id;
	}

	public void setPost_id(int post_id) {
		this.post_id = post_id;
	}

	public String getBoard_id() {
		return board_id;
	}

	public void setBoard_id(String board_id) {
		this.board_id = board_id;
	}

	public String getEmp_id() {
		return emp_id;
	}

	public void setEmp_id(String emp_id) {
		this.emp_id = emp_id;
	}

	public String getPost_title() {
		return post_title;
	}

	public void setPost_title(String post_title) {
		this.post_title = post_title;
	}

	public String getPost_content() {
		return post_content;
	}

	public void setPost_content(String post_content) {
		this.post_content = post_content;
	}

	public String getPost_date() {
		return post_date;
	}

	public void setPost_date(String post_date) {
		this.post_date = post_date;
	}

	public String getPost_cnt() {
		return post_cnt;
	}

	public void setPost_cnt(String post_cnt) {
		this.post_cnt = post_cnt;
	}

	public String getPost_subtitle() {
		return post_subtitle;
	}

	public void setPost_subtitle(String post_subtitle) {
		this.post_subtitle = post_subtitle;
	}

}
